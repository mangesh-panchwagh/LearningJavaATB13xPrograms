package ex_00_Task.Lab_12June;

// Implicit type casting
public class Lab_004_ITC_Widening {

	public static void main(String[] args) {
		
		// Convert int A =100 to double
		
		int num = 100;				 // Integer value
		double numDouble = 100;		// Implicitly casted to double
		
		System.out.println("Integer value : " + num);
		System.out.println("Double value : " + numDouble);
	}
}
